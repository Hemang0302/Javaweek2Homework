package TrianglePatternHomework;

public class RightTriangleStarPattern {
    void star(){
        System.out.println("*");
        System.out.println("* *");
        System.out.println("* * *");
        System.out.println("* * * *");
        System.out.println("* * * * *");
    }

    public static void main(String[] args) {
        RightTriangleStarPattern Star = new RightTriangleStarPattern();
        Star.star();
    }
}

